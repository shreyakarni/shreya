	   <h1>.*ABOUT Apartment management system.*</h1>
	   <img src="appi.jpg"></img>
	   <p><font color="black"/>The main objective of this application is to help the construction industry users to manage the operations and activities of apartments.</p>
	   <p>This is a simple user interface developed using .net technology.</p>
	   <p>It helps the user to keep track of all the sales, purchases, Billing, invoices, maintenance related information of the different apartments of a particular construction firm. </p>
	   <p>It helps the construction companies to reduce the cost of maintaining these records.</P>
	   
	   <p>There will be a robust database which stores all the data related to these various apartments, their maintenance related information etc. It helps to keep track of all the payments done by customers towards maintenance advances or purchases etc. This tool will ease the user to manage huge data of different customers who own apartments from the same builder. It will record the date of purchase, the amount paid, the mode of payment. It shall also have the data related to the installment the user has opted for. For the maintenance part of the interface it shall have the data of all the maintenance charges paid by the customers, whether its done quarterly or annually. It also records the mode of payment. It will also hold all the details such as the apartment number, the area of the house (sq. ft.) bought etc.</p>
